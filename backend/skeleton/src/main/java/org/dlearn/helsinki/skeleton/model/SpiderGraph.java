package org.dlearn.helsinki.skeleton.model;

public class SpiderGraph {
	
	public int _id;
	public int student_id;
	public int value1;
	public int value2;
	public int value3;
	public int value4;
	public int value5;
	
	public SpiderGraph() {
		super();
	}
	public SpiderGraph(int _id, int student_id, int value1, int value2,
			int value3, int value4, int value5) {
		super();
		this._id = _id;
		this.student_id = student_id;
		this.value1 = value1;
		this.value2 = value2;
		this.value3 = value3;
		this.value4 = value4;
		this.value5 = value5;
	}
	
	
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public int getStudent_id() {
		return student_id;
	}
	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}
	public int getValue1() {
		return value1;
	}
	public void setValue1(int value1) {
		this.value1 = value1;
	}
	public int getValue2() {
		return value2;
	}
	public void setValue2(int value2) {
		this.value2 = value2;
	}
	public int getValue3() {
		return value3;
	}
	public void setValue3(int value3) {
		this.value3 = value3;
	}
	public int getValue4() {
		return value4;
	}
	public void setValue4(int value4) {
		this.value4 = value4;
	}
	public int getValue5() {
		return value5;
	}
	public void setValue5(int value5) {
		this.value5 = value5;
	}
	
	
	
	

}
